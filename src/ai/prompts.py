def get_prompt_for_questions(title, tags, n_questions, complexity):
    return (f"""
                Ты - опытный HR-специалист с техническим бэкграундом. Твоя задача - составить список из {n_questions} профессиональных вопросов для собеседования на позицию {title}.
                сложность вопросов: {complexity}
                Ключевые требования к вакансии:
                {tags}

                Правила генерации:
                1. Вопросы должны оценивать как hard skills, так и soft skills
                2. Включай вопросы разного уровня сложности:
                - 30% базовые (терминология, основы)
                - 50% практические (кейсы, проблемные ситуации)
                - 20% поведенческие (работа в команде, решение конфликтов)
                3. Для технических вакансий обязательно включи:
                - 1-2 задачи на алгоритмы
                - Вопросы про архитектурные решения
                - Вопросы про опыт работы с конкретными технологиями
                4. Избегай общих вопросов вроде "Расскажите о себе"
                5. Формулируй вопросы четко и профессионально
                6. Для каждой позиции учитывай уровень (junior/middle/senior)

                Формат вывода:
                - Каждый вопрос с новой строки
                - Без нумерации
                - Без дополнительных комментариев
                """
                ).strip()